    private void cargarTabla() {
        
        DecimalFormat forma = new DecimalFormat("###,###.##");
        int i=0;
        while (!"".equals(modeloTablaComponentes.getValueAt(i, 0).toString())){
            tbComponentes.setValueAt(modeloTablaComponentes.getValueAt(i, 0), i, 0);
            tbComponentes.setValueAt(modeloTablaComponentes.getValueAt(i, 1), i, 1);
            String total = forma.format(Integer.parseInt(modeloTablaComponentes.getValueAt(i, 2).toString()));
            tbComponentes.setValueAt(total, i, 2);
            i++;
            centrarTabla();
            centrarTitulo();
        }
    }
