    private void centrarTitulo(){ 
        for (int i = 0 ; i < tbComponentes.getColumnCount(); i++){
            DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
            renderer.setHorizontalAlignment(SwingConstants.CENTER);
            tbComponentes.getColumn(i).setHeaderRenderer(renderer);
        }
    }
    
     private void getDeposito() {
        try {
            try (ResultSet rs = depositoControlador.getDepositos()) {

                modelCombo.removeAllElements();

                while (rs.next()) {
                    modelCombo.addElement(rs.getObject("Cod. Deposito").toString());
                }
                cboDeposito.setModel(modelCombo);
            } catch (Exception ex) {
                showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
            }
        } catch (HeadlessException ex) {
            showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
        }
    }
    
     private void getStock(String codigoDeposito) {
        try {

            tbComponentes.setModel(modeloTablaComponentes);

            modeloTablaComponentes.setRowCount(0);
            modeloTablaComponentes.setColumnCount(0);

            try (ResultSet rs = stockControlador.getStock(codigoDeposito)) {
                ResultSetMetaData rsMd = rs.getMetaData();

                int cantidadColumnas = rsMd.getColumnCount();

                //System.out.println("Cantidad columnas "+cantidadColumnas);
                for (int i = 1; i <= cantidadColumnas; i++) {
                    modeloTablaComponentes.addColumn(rsMd.getColumnLabel(i));
                }

                while (rs.next()) {
                    Object[] fila = new Object[cantidadColumnas];
                    for (int i = 0; i < cantidadColumnas; i++) {
                        fila[i] = rs.getObject(i + 1);
                    }
                    modeloTablaComponentes.addRow(fila);
                }

                
            } catch (Exception ex) {
                showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
            }
        } catch (HeadlessException ex) {
            showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
        }

    }
    
    
    
