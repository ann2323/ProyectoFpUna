     private void guardar(){
        if ("".equals(txtCodigo.getText())) {
            showMessageDialog(null, "Debe ingresar el codigo.", "Atención", INFORMATION_MESSAGE);
            txtCodigo.requestFocusInWindow();
            return;
        } else if ("".equals(txtNombre.getText())) {
            showMessageDialog(null, "Debe ingresar un nombre.", "Atención", INFORMATION_MESSAGE);
            txtNombre.requestFocusInWindow();
            return;
        } else if ("".equals(txtDescripcion.getText())) {
            showMessageDialog(null, "Debe ingresar la descripción.", "Atención", INFORMATION_MESSAGE);
            txtDescripcion.requestFocusInWindow();
            return;
        }
        else {
            dep.setCodigo(txtCodigo.getText());
            dep.setNombre(txtNombre.getText());
            dep.setDescripcion(txtDescripcion.getText());     
        
            if (bNuevo.isEnabled() == false) {
                try {
                    depBD.insert(dep);
                    nuevo();
                    getDepositos();
                } catch (Exception ex) {
                    showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
                }
            } else {
                try {
                    depBD.update(dep);
                    limpiar();
                    getDepositos();
                } catch (Exception ex) {
                    showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
                }
            }
         }
    }
    
    private void borrar(){
        if(showConfirmDialog (null, "Está seguro de eliminar el deposito seleccionado?", "Confirmar", YES_NO_OPTION) == YES_OPTION){
            try {
                depBD.delete(tbDepositos.getValueAt(tbDepositos.getSelectedRow(), 0).toString());
                limpiar();
                getDepositos();
            } catch (Exception ex) {
                showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
            }
        }
    }
    
    private void cancelar(){
        if(showConfirmDialog (null, "Está seguro de cancelar la operación?", "Confirmar", YES_NO_OPTION) == YES_OPTION){
            limpiar();
            getDepositos();
            establecerBotones("Edicion");
        }
    }
    
    private void nuevo() {
        limpiar();
        establecerBotones("Nuevo");
        txtCodigo.requestFocusInWindow();
    }
    
    private void limpiar() {
        txtCodigo.setText("");
        txtNombre.setText("");
        txtDescripcion.setText("");      
        
        if (tbDepositos.getRowCount() == 0){
            establecerBotones("Vacio");
        }
        
    }
    
    private void getDepositos() {
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            tbDepositos.setModel(modelo);
            try (ResultSet rs = depBD.datos()) {
                ResultSetMetaData rsMd = rs.getMetaData();
                
                int cantidadColumnas = rsMd.getColumnCount();
                
                for (int i = 1; i <= cantidadColumnas; i++) {
                    modelo.addColumn(rsMd.getColumnLabel(i));
                }

                while (rs.next()) {
                    Object[] fila = new Object[cantidadColumnas];
                    for (int i = 0; i < cantidadColumnas; i++) {
                        fila[i]=rs.getObject(i+1);
                    }
                    modelo.addRow(fila);
                }
            } catch (Exception ex) {
                showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
            }
        } catch (HeadlessException ex) {
            showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
        }
    }
    
    private void establecerBotones(String modo) {
        switch (modo) {
            case "Nuevo":
                bNuevo.setEnabled(false);
                bBorrar.setEnabled(false);
                bCancelar.setEnabled(true);
                bGuardar.setEnabled(true);
                break;
            case "Edicion":
                bNuevo.setEnabled(true);
                bBorrar.setEnabled(true);
                bCancelar.setEnabled(false);
                bGuardar.setEnabled(true);
                break;
            case "Vacio":
                bNuevo.setEnabled(true);
                bBorrar.setEnabled(false);
                bCancelar.setEnabled(false);
                bGuardar.setEnabled(false);
                break;
        }
    }

