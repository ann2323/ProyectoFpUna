    private void guardar(){
        if ("".equals(txtCodigo.getText())) {
            showMessageDialog(null, "Debe ingresar un código.", "Atención", INFORMATION_MESSAGE);
            txtCodigo.requestFocusInWindow();
            return;
        } else if ("".equals(dFecha.getText())) {
            showMessageDialog(null, "Debe ingresar la fecha de ingreso", "Atención", INFORMATION_MESSAGE);
            dFecha.requestFocusInWindow();
            return;
        } 
        else {
            try {
                sal.setCodigoInterno(salCont.nuevoCodigo());
            } catch (Exception ex) {
                 showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
            }
            try { 
                sal.setDeposito(depBD.getCodigo((String) comboDeposito.getSelectedItem()));
            } catch (Exception ex) {
                showMessageDialog(null, "setCodDeposito " + ex, "Error", ERROR_MESSAGE);
            }
            try {
                SimpleDateFormat a = new SimpleDateFormat("dd/MM/yyyy");
                Date fecha = a.parse(dFecha.getText());
                sal.setFechaIngreso(fecha);
            } catch (ParseException ex) {
                Logger.getLogger(salidaForm2.class.getName()).log(Level.SEVERE, null, ex);
            }
            sal.setNumero(Integer.parseInt(txtCodigo.getText()));
            sal.setComentarios(txtComentarios.getText());     
            sal.setAjuste(comboAjuste.getSelectedIndex()); 
            sal.setProyecto(txtProyecto.getText());     
            
            if (bNuevo.isEnabled() == false) {
                try {
                    int i = 0;
                    String dpsto = depBD.getCodigo((String) comboDeposito.getSelectedItem());
                    try {
                        int cod_int = salCont.nuevoCodigo();
                        while (!"".equals(tDetalleSalida.getValueAt(i, 0).toString())){
                            salD.setLine(salCont.nuevaLinea());
                            if (stockCont.tieneCodStock(tDetalleSalida.getValueAt(i, 0).toString(),dpsto) == 0){
                                showMessageDialog(null, "Existen componentes sin stock en el detalle", "Atención", INFORMATION_MESSAGE);
                                bGuardar.requestFocusInWindow();
                                return;
                            } else if (stockCont.tieneStock(tDetalleSalida.getValueAt(i, 0).toString(),dpsto) == 0){
                                showMessageDialog(null, "Existen componentes sin stock en el detalle", "Atención", INFORMATION_MESSAGE);
                                bGuardar.requestFocusInWindow();
                                return;
                            } else if (stockCont.tieneStock(tDetalleSalida.getValueAt(i, 0).toString(),dpsto) < Integer.parseInt(tDetalleSalida.getValueAt(i, 3).toString())){
                                showMessageDialog(null, "Existen componentes sin stock en el detalle", "Atención", INFORMATION_MESSAGE);
                                bGuardar.requestFocusInWindow();
                                return;
                            } else if ("".equals(tDetalleSalida.getValueAt(i, 0).toString()) || "".equals(tDetalleSalida.getValueAt(i, 1).toString())
                                    || "".equals(tDetalleSalida.getValueAt(i, 2).toString()) || "".equals(tDetalleSalida.getValueAt(i, 3).toString())){
                                showMessageDialog(null, "Completar datos", "Atención", INFORMATION_MESSAGE);
                                tDetalleSalida.requestFocusInWindow();
                                return;
                            }
                            i++;
                        }
                    } catch (Exception ex) {
                         showMessageDialog(null, ex, "Atención", INFORMATION_MESSAGE);
                         bGuardar.requestFocusInWindow();
                         return;
                    }
                } catch (Exception ex) {
                    showMessageDialog(null, ex, "Atención", INFORMATION_MESSAGE);
                    bGuardar.requestFocusInWindow();
                    return;
                }
                     
                try {
                    int i = 0;
                    String dpsto = depBD.getCodigo((String) comboDeposito.getSelectedItem());
                    try {
                        int cod_int = salCont.nuevoCodigo();
                        while (!"".equals(tDetalleSalida.getValueAt(i, 0).toString())){
                            salD.setCodigoInterno(cod_int);
                            salD.setLine(salCont.nuevaLinea());
                            salD.setCodigo(tDetalleSalida.getValueAt(i, 0).toString());
                            salD.setDescripcion(tDetalleSalida.getValueAt(i, 1).toString());
                            salD.setCantidad(Integer.parseInt(tDetalleSalida.getValueAt(i, 3).toString()));
                            salD.setPrecio(Integer.parseInt(tDetalleSalida.getValueAt(i, 2).toString()));
                            stockCont.update2(tDetalleSalida.getValueAt(i, 0).toString(), dpsto, Integer.parseInt(tDetalleSalida.getValueAt(i, 3).toString()));
                            salCont.insert(salD);
                            i++;
                        }
                    } catch (Exception ex) {
                        showMessageDialog(null, ex, "Atención", INFORMATION_MESSAGE);
                        bGuardar.requestFocusInWindow();
                        return;
                    }
                    salCont.cambiarAEnProceso(txtProyecto.getText());
                    salCont.insert(sal);
                    nuevo();
                    getSalidas();
                } catch (Exception ex) {
                    showMessageDialog(null, ex, "Atención", INFORMATION_MESSAGE);
                    bGuardar.requestFocusInWindow();
                    return;
                }
            } else {
                try {
                    sal.setCodigoInterno((Integer) modeloC.getValueAt(k, 0));
                    salCont.update(sal);
                    getSalidas();
                    datosActuales();
                    getSalidasDetalle();
                } catch (Exception ex) {
                    showMessageDialog(null, ex, "Atención", INFORMATION_MESSAGE);
                    bGuardar.requestFocusInWindow();
                    return;
                }
            }
         }
    }
    
    private void limpiar() {
        txtCodigo.setText("");
        txtProyecto.setText("");
        comboAjuste.setSelectedIndex(0);
        tDetalleSalida.removeAll();
        dFecha.setText("");
        txtComentarios.setText("");
        comboDeposito.setSelectedIndex(0);
    }
    
    private void cancelar(){
        if(showConfirmDialog (null, "Está seguro de cancelar la operación?", "Confirmar", YES_NO_OPTION) == YES_OPTION){
            getSalidas();
            if (modeloC.getRowCount() == 0) {
                limpiar(); 
                modoBusqueda(false); 
                establecerBotones("Vacio"); 
                return;
            }
            if (k >= 0){
                    limpiar(); 
                    datosActuales(); 
                    modoBusqueda(false);
                    establecerBotones("Edicion");
                    return;
            }
        }
    }
    
    private void buscar(){
        limpiar();
        modoBusqueda(true);
        establecerBotones("Buscar");
        nuevoDetalle();
    }
    
    private void modoBusqueda(boolean v){
        if (v == true) {
        txtCodigo.requestFocusInWindow();
        txtCodigo.setBackground(Color.yellow);
        txtProyecto.setEnabled(false);
        dFecha.setEnabled(false);
        txtComentarios.setEnabled(false);
        comboAjuste.setEnabled(false);
        comboDeposito.setEnabled(false);
        } else {
        txtCodigo.requestFocusInWindow();
        txtCodigo.setBackground(Color.white);
        txtProyecto.setEnabled(true);
        dFecha.setEnabled(true);
        txtComentarios.setEnabled(true);
        comboAjuste.setEnabled(true);
        comboDeposito.setEnabled(true);
        }
    }
    
    private void primero(){
        if (modeloC.getRowCount() == 0) {
            showMessageDialog(null, "No hay registros.", "Atención", INFORMATION_MESSAGE); 
            return;
        }    
            k = 0;
            datosActuales();
    }
    
    private void ultimo(){
         if (modeloC.getRowCount() == 0) {
             showMessageDialog(null, "No hay registros.", "Atención", INFORMATION_MESSAGE); 
             return;
         }  
            k = modeloC.getRowCount() - 1;
            datosActuales();
     }
    
    private void anterior(){
        if (modeloC.getRowCount() == 0) {
             showMessageDialog(null, "No hay registros.", "Atención", INFORMATION_MESSAGE); 
             return;
         }      
            k = k - 1;
            if (k < 0) {
                showMessageDialog(null, "Has llegado al primer registro.", "Atención", INFORMATION_MESSAGE);
                k = k + 1;
                return;
            }
            datosActuales();
    }
    
    private void siguiente(){
        if (modeloC.getRowCount() == 0) {
             showMessageDialog(null, "No hay registros.", "Atención", INFORMATION_MESSAGE); 
             return;
         }  
            k = k + 1;
            if (k == modeloC.getRowCount()) {
                showMessageDialog(null, "Has llegado al ultimo registro.", "Atención", INFORMATION_MESSAGE);
                k = k - 1;
                return;
            }
            datosActuales();
    }
    
    private void datosActuales(){
            txtCodigo.setText(modeloC.getValueAt(k, 1).toString());
            
            try {
                SimpleDateFormat formatoRecibido = new SimpleDateFormat("yyyy-MM-dd");
                SimpleDateFormat formatoFinal = new SimpleDateFormat("dd/MM/yyyy");
                Date fecha1;
                fecha1 = formatoRecibido.parse(modeloC.getValueAt(k, 2).toString());
                dFecha.setText(formatoFinal.format(fecha1));
             } catch (ParseException ex) {
                Logger.getLogger(entradaForm2.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            txtComentarios.setText(modeloC.getValueAt(k, 3).toString());
            comboAjuste.setSelectedIndex((int) modeloC.getValueAt(k, 4));
            try {
                comboDeposito.setSelectedItem(depBD.getDato((String) modeloC.getValueAt(k, 5)));
            } catch (Exception ex) {
                showMessageDialog(null, "Combo deposito no obtuvo datos", "Error", ERROR_MESSAGE); 
            }
            txtProyecto.setText(modeloC.getValueAt(k, 6).toString());
            getSalidasDetalle();
    }
    
    private void getSalidas() {
        try {
            try (ResultSet rs = salCont.datos()) {
                
                modeloC.setRowCount(0);
                modeloC.setColumnCount(0);
                
                ResultSetMetaData rsMd = rs.getMetaData();
                
                int cantidadColumnas = rsMd.getColumnCount();
                
                for (int i = 1; i <= cantidadColumnas; i++) {
                    modeloC.addColumn(rsMd.getColumnLabel(i));
                }

                while (rs.next()) {
                    Object[] fila = new Object[cantidadColumnas];
                    for (int i = 0; i < cantidadColumnas; i++) {
                        fila[i]=rs.getObject(i+1);
                    }
                    modeloC.addRow(fila);
                }
            } catch (Exception ex) {
                showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
            }
        } catch (HeadlessException ex) {
            showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
        }
    }
    
    private void getSalidasDetalle() {
        try {
            try (ResultSet rs = salCont.datosDetalle((int) modeloC.getValueAt(k, 0))) {
                
                modeloD.setRowCount(0);
                modeloD.setColumnCount(0);
                
                ResultSetMetaData rsMd = rs.getMetaData();
                
                int cantidadColumnas = rsMd.getColumnCount();
                
                for (int i = 1; i <= cantidadColumnas; i++) {
                    modeloD.addColumn(rsMd.getColumnLabel(i));
                }

                while (rs.next()) {
                    Object[] fila = new Object[cantidadColumnas];
                    for (int i = 0; i < cantidadColumnas; i++) {
                        fila[i]=rs.getObject(i+1);
                    }
                    modeloD.addRow(fila);
                }
                tDetalleSalida.setModel(modeloD);
            } catch (Exception ex) {
                showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
            }
        } catch (HeadlessException ex) {
            showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
        }
    }
    
    private void nuevoDetalle() {
        try {
            try (ResultSet rs = salCont.datosD()) {
                
                modeloD.setRowCount(0);
                modeloD.setColumnCount(0);
                
                ResultSetMetaData rsMd = rs.getMetaData();
                
                int cantidadColumnas = rsMd.getColumnCount();
                
                for (int i = 1; i <= cantidadColumnas; i++) {
                    modeloD.addColumn(rsMd.getColumnLabel(i));
                }
                    for (int i = 0; i < 12; i++) {
                        modeloD.addRow(new Object[]{"","","",""});
                    }

                tDetalleSalida.setModel(modeloD);
            } catch (Exception ex) {
                showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
            }
        } catch (HeadlessException ex) {
            showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
        }
    }
    
    private void getDepositos() {
        try {
            try (ResultSet rs = depBD.datoCombo()) {
                
                modelCombo.removeAllElements();
                
                while (rs.next()) {
                    modelCombo.addElement(rs.getObject("dato").toString());
                }
            comboDeposito.setModel(modelCombo);
            } catch (Exception ex) {
                showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
            }
        } catch (HeadlessException ex) {
            showMessageDialog(null, ex, "Error", ERROR_MESSAGE);
        }
    }
    
