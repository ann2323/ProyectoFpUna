    private void tDetalleSalidaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tDetalleSalidaKeyPressed
        if (tDetalleSalida.isCellSelected(tDetalleSalida.getSelectedRow(), 1) == true){
            if (evt.getKeyCode() == KeyEvent.VK_TAB) {
                if ("".equals(tDetalleSalida.getValueAt(tDetalleSalida.getSelectedRow(), 0).toString()))
                {
                    BuscarForm bf = new BuscarForm(null, true);
                    bf.columnas = "codigo, descripcion";
                    bf.tabla = "componentes where servicio = 0 --";
                    bf.order = "codigo_interno";
                    bf.filtroBusqueda = "";
                    bf.setLocationRelativeTo(this);
                    bf.setVisible(true);

                    tDetalleSalida.setValueAt(bf.retorno, tDetalleSalida.getSelectedRow(), 0);
                    ComponentesControlador cmpCont = new ComponentesControlador();
                    tDetalleSalida.setValueAt(cmpCont.getDescripcion(bf.retorno), tDetalleSalida.getSelectedRow(), 1);
                    tDetalleSalida.setValueAt(cmpCont.getCosto(bf.retorno), tDetalleSalida.getSelectedRow(), 2);
                    tDetalleSalida.setEditingColumn(3);
                }
            }
        }
    }//GEN-LAST:event_tDetalleSalidaKeyPressed

