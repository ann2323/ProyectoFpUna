    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String contrasenha = new String(txtContrasenha.getPassword());
        
         if (txtUsuario.getText().trim().isEmpty() == true) {
                showMessageDialog(this, "Por favor ingrese su usuario", "Atención", JOptionPane.WARNING_MESSAGE);
                return;
          }
         
         if (contrasenha.trim().isEmpty() == true) {
                showMessageDialog(this, "Por favor ingrese su contraseña", "Atención", JOptionPane.WARNING_MESSAGE);
                return;
          }
        Usuario usu = new Usuario();
        usu.setNombreusuario(txtUsuario.getText());
        usu.setContrasenha(contrasenha);
        
        if(usuarioControlador.login(usu)== false){
            showMessageDialog(this, "Usuario y/o contraseña incorrecta ", "Error de login", JOptionPane.ERROR_MESSAGE);
        }else{
            showMessageDialog(this, "Bienvenido " + usu.getNombreusuario());
            MenuPrincipalForm principal  = new MenuPrincipalForm();
            principal.nombreUsuario = txtUsuario.getText();
            try {
                principal.codRol = usuarioControlador.getRolId(txtUsuario.getText());
                principal.rolUsuario = usuarioControlador.getRolNombre(principal.codRol);
                System.out.println(principal.codRol);
                System.out.println(principal.rolUsuario);
            } catch (Exception ex) {
                Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            principal.setVisible(true);
            this.dispose();
            this.setVisible(false);
            
            
        }
    }//GEN-LAST:event_btnLoginActionPerformed

